void main() {
  int x = 10;
  int y = 20;


  // Equal to
  print('x == y: ${x == y}');
  // Not equal to
  print('x != y: ${x != y}');
  // Greater than
  print('x > y: ${x > y}');
  // Less than
  print('x < y: ${x < y}');
  // Greather than or equal to
  print('x >= 10: ${x >= 10}');
  // Less than or equal to
  print('x <= 10: ${x <= 10}');
}