void main() {
  // assignment
  double angka = 100;
  print("initial number = $angka");

  // add and assignment
  angka += 10;
  print("add and assignment = $angka");

  // substrac and assignment
  angka -=10;
  print("substrac and assignment = $angka");

  // multiply and assignment
  angka *= 2;
  print("multiply and assignment = $angka");

  // divide and assignment
  angka /= 2;
  print("divide and assignment = $angka");

  // modulo and assignment
  angka %= 7;
  print("modulo and assignment = $angka");

  // integer divide and assignment
  int angka_int = 10;
  angka_int ~/= 3;
  print("integer divide and assignment = $angka_int");


}