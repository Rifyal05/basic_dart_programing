import 'dart:math';

void main() {
  // konstanta matemtika
  print("Pi: ${pi}");
  print("e: ${e}");
  // trigonometri
  print("Sin(pi/2): ${sin(pi / 2)}");
  print("Cos(0): ${cos(0)}");
  // Eksponensial dan Logaritmik
  print("exp(1): ${exp(1)}");
  print("log(10): ${log(10)}");
  print("pow(2, 3): ${pow(2, 3)}");
  print("sqrt(16): ${sqrt(16)}");
  // min dan max
  print("max(10, 5): ${max(10, 5)}");
  print("min(10, 5): ${min(10, 5)}");
}